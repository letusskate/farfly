"""
Trace-driven FarFly experiment.

Replays an event timeline (generated by trace_adapter.py) against a running
FarFly cluster and collects per-request metrics.

Usage:
    python trace_experiment.py --config-dir trace_configs --duration 60 --output results.json
"""
from __future__ import annotations

import argparse
import collections
import json
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path
import time
from typing import Any, Dict, Iterable, List
import uuid

from farfly_client import SchedulerClient, ServerClient, load_json_file


BASE_DIR = Path(__file__).resolve().parent


def load_events(events_path: Path) -> List[Dict[str, Any]]:
    with open(events_path, "r", encoding="utf-8") as fh:
        return json.load(fh)


def execute_event(
    client: SchedulerClient,
    monotonic_start: float,
    event: Dict[str, Any],
) -> Dict[str, Any]:
    wake_at = monotonic_start + float(event["scheduled_offset"])
    remaining = wake_at - time.perf_counter()
    if remaining > 0:
        time.sleep(remaining)

    payload: Dict[str, Any] = {
        "request_id": str(uuid.uuid4()),
        "service": event["service"],
        "task_type": event["task_type"],
        "timestamp": time.time(),
        "timeout_ms": event["timeout_ms"],
        "payload": dict(event.get("payload", {})),
    }
    if "flow_id" in event:
        payload["flow_id"] = event["flow_id"]
    if "frame_id" in event:
        payload["frame_id"] = event["frame_id"]

    timeout_seconds = max(float(event["timeout_ms"]) / 1000.0 * 2.0, 30.0)
    started_at = time.perf_counter()
    try:
        status_code, body = client.submit(payload, timeout=timeout_seconds)
    except Exception as exc:
        return {
            "service": event["service"],
            "task_type": event["task_type"],
            "scheduled_offset": event["scheduled_offset"],
            "flow_id": event.get("flow_id"),
            "frame_id": event.get("frame_id"),
            "status_code": 599,
            "duration_ms": (time.perf_counter() - started_at) * 1000.0,
            "error": str(exc),
        }

    metrics = body.get("metrics", {}) if isinstance(body, dict) else {}
    return {
        "service": event["service"],
        "task_type": event["task_type"],
        "scheduled_offset": event["scheduled_offset"],
        "flow_id": event.get("flow_id"),
        "frame_id": event.get("frame_id"),
        "status_code": status_code,
        "duration_ms": (time.perf_counter() - started_at) * 1000.0,
        "scheduled_server": body.get("scheduled_server") if isinstance(body, dict) else None,
        "violated_slo": bool(metrics.get("violated_slo", False)),
        "queue_delay_ms": float(metrics.get("queue_delay_ms", 0.0)),
        "processing_time_ms": float(metrics.get("processing_time_ms", 0.0)),
        "available_credits": metrics.get("available_credits"),
    }


def summarize_results(
    results: Iterable[Dict[str, Any]],
    elapsed_seconds: float,
) -> Dict[str, Any]:
    result_list = list(results)
    http_status_counts = collections.Counter(item["status_code"] for item in result_list)
    service_summary: Dict[str, Dict[str, Any]] = {}

    durations_all: List[float] = []

    for item in result_list:
        service_name = item["service"]
        summary = service_summary.setdefault(
            service_name,
            {
                "task_type": item["task_type"],
                "submitted": 0,
                "success": 0,
                "violations": 0,
                "total_queue_delay_ms": 0.0,
                "total_processing_time_ms": 0.0,
                "total_duration_ms": 0.0,
                "durations": [],
                "queue_delays": [],
                "server_distribution": collections.Counter(),
            },
        )
        summary["submitted"] += 1
        if item["status_code"] < 400 and not item.get("error"):
            summary["success"] += 1
            summary["violations"] += 1 if item.get("violated_slo", False) else 0
            summary["total_queue_delay_ms"] += item.get("queue_delay_ms", 0.0)
            summary["total_processing_time_ms"] += item.get("processing_time_ms", 0.0)
            summary["total_duration_ms"] += item.get("duration_ms", 0.0)
            summary["durations"].append(item.get("duration_ms", 0.0))
            summary["queue_delays"].append(item.get("queue_delay_ms", 0.0))
            durations_all.append(item.get("duration_ms", 0.0))
            if item.get("scheduled_server"):
                summary["server_distribution"][item["scheduled_server"]] += 1

    for svc_name, summary in service_summary.items():
        success = max(1, summary["success"])
        summary["avg_queue_delay_ms"] = round(summary["total_queue_delay_ms"] / success, 2)
        summary["avg_processing_time_ms"] = round(summary["total_processing_time_ms"] / success, 2)
        summary["avg_duration_ms"] = round(summary["total_duration_ms"] / success, 2)
        # percentiles
        durs = sorted(summary["durations"])
        qds = sorted(summary["queue_delays"])
        if durs:
            summary["p50_duration_ms"] = round(durs[len(durs) // 2], 2)
            summary["p99_duration_ms"] = round(durs[min(len(durs) - 1, int(len(durs) * 0.99))], 2)
        if qds:
            summary["p50_queue_delay_ms"] = round(qds[len(qds) // 2], 2)
            summary["p99_queue_delay_ms"] = round(qds[min(len(qds) - 1, int(len(qds) * 0.99))], 2)
        summary["slo_violation_rate"] = round(summary["violations"] / summary["submitted"], 4)
        summary["server_distribution"] = dict(summary["server_distribution"])
        del summary["durations"]
        del summary["queue_delays"]
        del summary["total_queue_delay_ms"]
        del summary["total_processing_time_ms"]
        del summary["total_duration_ms"]

    success_count = sum(1 for item in result_list if item["status_code"] < 400 and not item.get("error"))
    violation_count = sum(1 for item in result_list if item.get("violated_slo", False))
    durations_sorted = sorted(durations_all)
    return {
        "submitted": len(result_list),
        "elapsed_seconds": round(elapsed_seconds, 2),
        "requests_per_second": round(len(result_list) / elapsed_seconds, 2) if elapsed_seconds > 0 else 0,
        "success_count": success_count,
        "failure_count": len(result_list) - success_count,
        "violation_count": violation_count,
        "slo_violation_rate": round(violation_count / max(1, len(result_list)), 4),
        "avg_duration_ms": round(sum(durations_all) / max(1, len(durations_all)), 2),
        "p50_duration_ms": round(durations_sorted[len(durations_sorted) // 2], 2) if durations_sorted else 0,
        "p99_duration_ms": round(
            durations_sorted[min(len(durations_sorted) - 1, int(len(durations_sorted) * 0.99))], 2
        ) if durations_sorted else 0,
        "status_code_counts": dict(http_status_counts),
        "service_summary": service_summary,
    }


def main() -> None:
    parser = argparse.ArgumentParser(description="Trace-driven FarFly experiment")
    parser.add_argument("--config-dir", required=True, help="Directory with trace_configs (from trace_adapter.py)")
    parser.add_argument("--max-workers", type=int, default=64, help="Client concurrency")
    parser.add_argument("--output", help="Path to write JSON results")
    parser.add_argument("--limit-events", type=int, default=0, help="Limit number of events (0=all)")
    args = parser.parse_args()

    config_dir = Path(args.config_dir).resolve()
    events_path = config_dir / "trace_events.json"
    sender_path = config_dir / "config_sender.json"
    scheduler_config_path = config_dir / "trace_scheduler_config.json"

    sender_config = load_json_file(sender_path)
    events = load_events(events_path)
    if args.limit_events > 0:
        events = events[: args.limit_events]

    print(f"Loaded {len(events)} events from {events_path}")
    print(f"Scheduler: {sender_config['scheduler']['ip']}:{sender_config['scheduler']['port']}")

    scheduler_client = SchedulerClient.from_config(sender_config)
    monotonic_start = time.perf_counter() + 1.0  # 1s grace
    wall_start = time.time()
    results: List[Dict[str, Any]] = []

    try:
        with ThreadPoolExecutor(max_workers=max(1, args.max_workers)) as executor:
            futures = [
                executor.submit(execute_event, scheduler_client, monotonic_start, event)
                for event in events
            ]
            done = 0
            for future in as_completed(futures):
                results.append(future.result())
                done += 1
                if done % 500 == 0:
                    print(f"  … {done}/{len(events)} events completed")

        elapsed_seconds = time.time() - wall_start
        summary = summarize_results(results, elapsed_seconds)

        # Collect cluster metrics
        summary["experiment"] = {
            "config_dir": str(config_dir),
            "total_events": len(events),
            "max_workers": args.max_workers,
        }

        # scheduler metrics
        try:
            _, sched_metrics = scheduler_client.metrics()
            summary["scheduler_metrics"] = sched_metrics
        except Exception:
            pass

        # server metrics
        server_metrics: Dict[str, Any] = {}
        for srv_info in sender_config.get("servers", []):
            srv_client = ServerClient.from_server_definition(srv_info)
            try:
                _, m = srv_client.metrics()
                server_metrics[srv_info["name"]] = m
            except Exception:
                pass
            finally:
                srv_client.close()
        summary["server_metrics"] = server_metrics
    finally:
        scheduler_client.close()

    output_text = json.dumps(summary, ensure_ascii=False, indent=2)
    print("\n" + "=" * 60)
    print(f"Results: {summary['submitted']} submitted, "
          f"{summary['success_count']} success, "
          f"{summary['failure_count']} failed, "
          f"{summary['violation_count']} SLO violations "
          f"({summary['slo_violation_rate']*100:.1f}%)")
    print(f"Throughput: {summary['requests_per_second']} req/s")
    print(f"Latency: avg={summary['avg_duration_ms']}ms  "
          f"p50={summary['p50_duration_ms']}ms  "
          f"p99={summary['p99_duration_ms']}ms")
    print("=" * 60)

    for svc_name, svc_data in summary["service_summary"].items():
        vr = svc_data["slo_violation_rate"] * 100
        print(f"  {svc_name}: {svc_data['submitted']} req, "
              f"violations={svc_data['violations']}({vr:.1f}%), "
              f"avg_q={svc_data['avg_queue_delay_ms']}ms")

    if args.output:
        out_path = Path(args.output).resolve()
        out_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.write_text(output_text + "\n", encoding="utf-8")
        print(f"\nResults saved to {out_path}")


if __name__ == "__main__":
    main()
